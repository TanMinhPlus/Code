#include<iostream>
using namespace std;

int main(){
    int x = 4;
    int y = 5;
    cout << "Using plus operator on X: " << +x << endl;
    cout << "Using minus operator on X: " << -x << endl;
    cout << "Using plus operator on Y: " << +y << endl;
    cout << "Using minus operator on Y: " << -y;
    return 0;
}